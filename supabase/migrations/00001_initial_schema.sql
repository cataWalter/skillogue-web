-- Create policies for public read access to passions and languages
CREATE POLICY "Allow public read access to passions" ON passions
    FOR SELECT USING (true);

CREATE POLICY "Allow public read access to languages" ON languages
    FOR SELECT USING (true);

CREATE POLICY "Allow public read access to locations" ON locations
    FOR SELECT USING (true);

-- Create policies for profiles
CREATE POLICY "Allow public read access to profiles" ON profiles
    FOR SELECT USING (true);

CREATE POLICY "Allow users to update own profile" ON profiles
    FOR UPDATE USING (auth.uid() = id);

-- Create policies for user_passions
CREATE POLICY "Allow public read access to user_passions" ON user_passions
    FOR SELECT USING (true);

CREATE POLICY "Allow users to manage own passions" ON user_passions
    FOR ALL USING (auth.uid() = user_id);

-- Create policies for profile_languages
CREATE POLICY "Allow public read access to profile_languages" ON profile_languages
    FOR SELECT USING (true);

CREATE POLICY "Allow users to manage own languages" ON profile_languages
    FOR ALL USING (auth.uid() = profile_id);

-- Create policies for messages
CREATE POLICY "Allow users to read own messages" ON messages
    FOR SELECT USING (auth.uid() = sender_id OR auth.uid() = receiver_id);

CREATE POLICY "Allow users to send messages" ON messages
    FOR INSERT WITH CHECK (auth.uid() = sender_id);

-- Create policies for notifications
CREATE POLICY "Allow users to read own notifications" ON notifications
    FOR SELECT USING (auth.uid() = user_id);

CREATE POLICY "Allow users to update own notifications" ON notifications
    FOR UPDATE USING (auth.uid() = user_id);

-- Create policies for favorites
CREATE POLICY "Allow users to read own favorites" ON favorites
    FOR SELECT USING (auth.uid() = user_id);

CREATE POLICY "Allow users to manage own favorites" ON favorites
    FOR ALL USING (auth.uid() = user_id);

-- Create policies for saved_searches
CREATE POLICY "Allow users to manage own saved searches" ON saved_searches
    FOR ALL USING (auth.uid() = user_id);

-- Create policies for reports
CREATE POLICY "Allow users to create reports" ON reports
    FOR INSERT WITH CHECK (auth.uid() = reporter_id);

-- Create policies for verification_requests
CREATE POLICY "Allow users to create verification requests" ON verification_requests
    FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Allow users to read own verification requests" ON verification_requests
    FOR SELECT USING (auth.uid() = user_id);
