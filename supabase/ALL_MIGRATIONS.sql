    WHERE p.id != current_user_id
    AND bu.id IS NULL
    AND my_up.passion_id IS NOT NULL
    GROUP BY p.id, p.first_name, p.last_name, p.about_me, p.avatar_url
    ORDER BY shared_passions_count DESC, RANDOM()
    LIMIT p_limit;
END;
$$;

