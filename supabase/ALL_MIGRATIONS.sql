-- RPC FUNCTION: search_profiles
-- =====================================================
CREATE OR REPLACE FUNCTION search_profiles(
    p_query TEXT DEFAULT NULL,
    p_location TEXT DEFAULT NULL,
    p_min_age INT DEFAULT NULL,
    p_max_age INT DEFAULT NULL,
    p_gender TEXT DEFAULT NULL,
    p_passion_ids BIGINT[] DEFAULT NULL,
    p_language TEXT DEFAULT NULL,
    p_limit INT DEFAULT 20,
    p_offset INT DEFAULT 0
)
RETURNS TABLE(
    id TEXT,
    first_name TEXT,
    last_name TEXT,
    about_me TEXT,
    location TEXT,
    age INT,
    gender TEXT,
    profile_languages TEXT[],
    created_at TIMESTAMPTZ,
    profilepassions TEXT[]
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    SELECT 
        p.id::TEXT,
        p.first_name,
        p.last_name,
        p.about_me,
        COALESCE(l.city, '') || ', ' || COALESCE(l.country, ''),
        p.age,
        p.gender,
        ARRAY_AGG(DISTINCT lang.name) FILTER (WHERE lang.name IS NOT NULL),
        p.created_at,
        ARRAY_AGG(DISTINCT passion.name) FILTER (WHERE passion.name IS NOT NULL)
    FROM profiles p
    LEFT JOIN locations l ON l.id = p.location_id
    LEFT JOIN profile_languages pl ON pl.profile_id = p.id
    LEFT JOIN languages lang ON lang.id = pl.language_id
    LEFT JOIN user_passions up ON up.user_id = p.id
    LEFT JOIN passions passion ON passion.id = up.passion_id
    LEFT JOIN blocked_users bu ON (bu.blocker_id = auth.uid() AND bu.blocked_id = p.id) OR (bu.blocker_id = p.id AND bu.blocked_id = auth.uid())
    WHERE bu.id IS NULL
    AND p.id != COALESCE(auth.uid(), p.id)
    AND (p_query IS NULL OR p.first_name ILIKE '%' || p_query || '%' OR p.last_name ILIKE '%' || p_query || '%' OR p.about_me ILIKE '%' || p_query || '%')
    AND (p_location IS NULL OR l.city ILIKE '%' || p_location || '%' OR l.country ILIKE '%' || p_location || '%')
    AND (p_min_age IS NULL OR p.age >= p_min_age)
    AND (p_max_age IS NULL OR p.age <= p_max_age)
    AND (p_gender IS NULL OR p.gender = p_gender)
    AND (p_passion_ids IS NULL OR up.passion_id = ANY(p_passion_ids))
    GROUP BY p.id, p.first_name, p.last_name, p.about_me, l.city, l.country, p.age, p.gender, p.created_at
    ORDER BY p.created_at DESC
    LIMIT p_limit OFFSET p_offset;
END;
$$;

