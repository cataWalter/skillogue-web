-- ANALYTICS AGGREGATED VIEWS (Materialized)
-- =====================================================

-- Daily active users view
CREATE MATERIALIZED VIEW IF NOT EXISTS daily_active_users AS
SELECT 
    DATE(created_at) as date,
    COUNT(DISTINCT user_id) as active_users
FROM analytics_events
WHERE user_id IS NOT NULL
GROUP BY DATE(created_at);

-- Daily event counts view
CREATE MATERIALIZED VIEW IF NOT EXISTS daily_event_counts AS
SELECT 
    DATE(created_at) as date,
    event_name,
    COUNT(*) as event_count
FROM analytics_events
GROUP BY DATE(created_at), event_name;

-- Refresh function for materialized views
CREATE OR REPLACE FUNCTION refresh_analytics_views()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    REFRESH MATERIALIZED VIEW daily_active_users;
    REFRESH MATERIALIZED VIEW daily_event_counts;
END;
$$;

-- =====================================================
-- USER ENGAGEMENT METRICS
-- =====================================================

-- Function to get user engagement score
CREATE OR REPLACE FUNCTION get_user_engagement_score(user_uuid UUID)
RETURNS TABLE(
    profile_views INT,
    messages_sent INT,
    messages_received INT,
    searchesPerformed INT,
    engagement_score DECIMAL
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    profile_view_count INT;
    msg_sent_count INT;
    msg_received_count INT;
    search_count INT;
BEGIN
    -- Count profile views (from analytics)
    SELECT COUNT(*)::INT INTO profile_view_count
    FROM analytics_events
    WHERE user_id = user_uuid AND event_name = 'profile_view';
    
    -- Count messages sent
    SELECT COUNT(*)::INT INTO msg_sent_count
    FROM messages
    WHERE sender_id = user_uuid;
    
    -- Count messages received
    SELECT COUNT(*)::INT INTO msg_received_count
    FROM messages
    WHERE receiver_id = user_uuid;
    
    -- Count searches
    SELECT COUNT(*)::INT INTO search_count
    FROM analytics_events
    WHERE user_id = user_uuid AND event_name = 'search';
    
    -- Calculate engagement score (0-100)
    RETURN QUERY
    SELECT 
        profile_view_count,
        msg_sent_count,
        msg_received_count,
        search_count,
        LEAST(100, 
            (profile_view_count * 1) + 
            (msg_sent_count * 5) + 
            (msg_received_count * 3) + 
            (search_count * 2)
        )::DECIMAL as engagement_score;
END;
$$;

