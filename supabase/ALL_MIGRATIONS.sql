CREATE OR REPLACE FUNCTION get_suggested_profiles(current_user_id UUID, p_limit INT DEFAULT 10)
RETURNS TABLE(
    id TEXT,
    first_name TEXT,
    last_name TEXT,
    about_me TEXT,
    avatar_url TEXT,
    shared_passions_count INT
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    SELECT 
        p.id::TEXT,
        p.first_name,
        p.last_name,
        p.about_me,
        p.avatar_url,
        COUNT(up.passion_id)::INT as shared_passions_count
    FROM profiles p
    LEFT JOIN user_passions up ON up.user_id = p.id
    LEFT JOIN user_passions my_up ON my_up.user_id = current_user_id AND my_up.passion_id = up.passion_id
