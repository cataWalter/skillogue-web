    DELETE FROM push_subscriptions WHERE user_id = current_user;
    DELETE FROM reports WHERE reporter_id = current_user OR reported_id = current_user;
    DELETE FROM verification_requests WHERE user_id = current_user;
    DELETE FROM profiles WHERE id = current_user;
END;
$$;

