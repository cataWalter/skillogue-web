-- RPC FUNCTION: get_recent_conversations
-- =====================================================
CREATE OR REPLACE FUNCTION get_recent_conversations(current_user_id UUID)
RETURNS TABLE(
    conversation_id TEXT,
    user_id TEXT,
    first_name TEXT,
    last_name TEXT,
    avatar_url TEXT,
    last_message TEXT,
    last_message_time TIMESTAMPTZ,
    unread_count BIGINT
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    WITH conversation AS (
        SELECT 
            CASE WHEN m.sender_id = current_user_id THEN m.receiver_id ELSE m.sender_id END as other_user_id,
            m.content as last_message,
            m.created_at as last_message_time,
            ROW_NUMBER() OVER (
                PARTITION BY CASE WHEN m.sender_id = current_user_id THEN m.receiver_id ELSE m.sender_id END 
                ORDER BY m.created_at DESC
            ) as rn
        FROM messages m
        WHERE m.sender_id = current_user_id OR m.receiver_id = current_user_id
    ),
    unread AS (
        SELECT receiver_id, COUNT(*) as cnt
        FROM messages
        WHERE receiver_id = current_user_id
        AND id NOT IN (SELECT message_id FROM message_reads WHERE user_id = current_user_id)
        GROUP BY receiver_id
    )
    SELECT 
        c.other_user_id::TEXT as conversation_id,
        p.id::TEXT as user_id,
        p.first_name,
        p.last_name,
        p.avatar_url,
        c.last_message,
        c.last_message_time,
        COALESCE(u.cnt, 0)::BIGINT as unread_count
    FROM conversation c
    JOIN profiles p ON p.id = c.other_user_id
    LEFT JOIN unread u ON u.receiver_id = c.other_user_id
    WHERE c.rn = 1
    ORDER BY c.last_message_time DESC;
END;
$$;

